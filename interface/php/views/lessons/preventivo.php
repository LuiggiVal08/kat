        <article class="temario__article">
            <img class="temario__img temario__img--rtl" src="/kat/resource/img/temario/limpieza-fisica.png"
                alt="" srcset="">
            <h3 class="article__title">Limpieza física de componentes</h3>
            <p class="article__text">
                Consiste en eliminar el polvo y la suciedad de los componentes físicos
                de un sistema informático, como ventiladores, disipadores de calor y teclados. Previene el
                sobrecalentamiento y la obstrucción de los componentes, lo que puede causar fallos en el
                sistema y reducir su vida útil.
                <br>
                <br>
                <b>Procedimientos:</b> Utilización de aire comprimido, cepillos antiestáticos y otros
                dispositivos
                para eliminar el polvo de manera segura y eficaz.
            </p>
        </article>
        <div class="clear"></div>
        <article class="temario__article">
            <img class="temario__img  temario__img--ltr" src="/kat/resource/img/temario/update-sofware.png"
                alt="" srcset="">
            <h3 class="article__title">Actualización de software
            </h3>
            <p class="article__text">
                Consiste en instalar parches de seguridad, actualizaciones del sistema operativo y del
                software para corregir errores, mejorar el rendimiento y cerrar vulnerabilidades de
                seguridad. Garantiza que el sistema esté protegido contra amenazas cibernéticas y que
                funcione de manera óptima con las últimas características y mejoras.
            </p>
        </article>
        <div class="clear"></div>
        <article class="temario__article">
            <img class="temario__img temario__img--low temario__img--rtl" src="/kat/resource/img/temario/respaldo.png"
                alt="" srcset="">
            <h3 class="article__title">Respaldo de datos</h3>
            <p class="article__text">
                Proceso de copiar y almacenar archivos importantes en un medio seguro para protegerlos
                contra la pérdida debido a fallos de hardware, ataques de malware o errores humanos.
                Asegura la disponibilidad y la integridad de los datos críticos en caso de
                desastres o incidentes de seguridad.
            </p>
        </article>
        <div class="clear"></div>
        <article class="temario__article">
            <img class="temario__img  temario__img--ltr" src="/kat/resource/img/temario/discos.png" alt=""
                srcset="">
            <h3 class="article__title">Revisión de hardware
            </h3>
            <p class="article__text">
                Proceso de inspección y evaluación de los componentes físicos de un sistema informático,
                como discos duros, memoria RAM y tarjetas de expansión, para detectar signos de desgaste o
                falla. Permite identificar y solucionar problemas potenciales antes de que causen un
                fallo completo del sistema, minimizando el tiempo de inactividad y los costos de reparación.
            </p>
        </article class="temario__article">
