            <section id="perifericos" class="temario__section">
                <h2 class="temario__title"> Interacción con el Mundo Partes Externas</h2>
                <hr>
                <img class="temario__img temario__img--rtl" src="/kat/resource/img/temario/monitor.png" alt="" srcset="">

                <article class="temario__article">
                    <h3 class="article__title">3.1 El Monitor: Ventana al mundo digital</h3>
                    <p class="article__text">El monitor de una computadora es un dispositivo de salida que muestra
                        información visual
                        generada por la computadora. Es un tipo de pantalla que presenta imágenes, texto y gráficos
                        producidos por la tarjeta gráfica del sistema. Los monitores pueden ser de diferentes tipos
                        y tecnologías, como LCD (pantalla de cristal líquido), LED (diodo emisor de luz), OLED
                        (diodo orgánico emisor de luz) o CRT (tubo de rayos catódicos), y varían en tamaño,
                        resolución y características adicionales como la frecuencia de actualización y la calidad
                        del color.</p>
                </article>
                <div class="clear"></div>
                <article class="temario__article">
                    <h3 class="article__title">3.2 Teclado y Ratón: Puentes de comunicación</h3>
                    <p class="article__text">
                    <ul class="article__list">
                        <li class="list__item"><b>Teclado:</b> Es un dispositivo que consta de una serie de teclas
                            que representan
                            letras, números,
                            símbolos y funciones especiales. Permite al usuario introducir texto y comandos mediante
                            la
                            pulsación de teclas. Los teclados modernos también pueden tener teclas multimedia y
                            teclas
                            de acceso rápido para funciones específicas.

                        </li>
                        <img class="temario__img temario__img--ltr" src="/kat/resource/img/temario/mouse-y-teclado.png"
                            alt="">
                        <li class="list__item"><b>Ratón:</b> Es un dispositivo pequeño que se desplaza sobre una
                            superficie plana y
                            permite al
                            usuario controlar el movimiento del cursor en la pantalla. El ratón generalmente tiene
                            uno o
                            más botones que pueden ser utilizados para realizar acciones como hacer clic, doble clic
                            o
                            arrastrar elementos en la pantalla. Además, algunos ratones pueden tener ruedas de
                            desplazamiento para facilitar la navegación. El ratón es especialmente útil para
                            seleccionar, arrastrar y manipular objetos en entornos gráficos.</li>
                    </ul>
                    </p>
                </article>
