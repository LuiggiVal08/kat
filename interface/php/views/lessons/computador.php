                <h2 class="temario__title">El Computador y sus partes</h2>
                <hr>
                <article class="temario__article">
                    <img class="temario__img temario__img--ltr" src="/kat/resource/img/temario/computador.jpg" alt=""
                        srcset="">
                    <p class="article__text">Una computadora es un dispositivo electrónico capaz de procesar, almacenar
                        y manipular datos
                        mediante la ejecución de programas y algoritmos. Sus componentes internos incluyen la CPU para
                        procesamiento, la memoria RAM para almacenamiento temporal y el disco duro para almacenamiento
                        permanente. Los periféricos externos incluyen dispositivos de entrada como el teclado y el
                        ratón, y dispositivos de salida como el monitor y la impresora.
                    </p>
                </article>
            </section>
            <div class="clear"></div>
            <section id="partes" class="temario__section">
                <h2 class="temario__title"> Viaje al Interior Partes Internas</h2>
                <hr>
                <article class="temario__article">
                    <img class="temario__img temario__img--rtl" src="/kat/resource/img/temario/placa-base.png" alt=""
                        srcset="">
                    <h3 class="article__title">2.1 La Placa Base: El esqueleto de la computadora</h3>
                    <p class="article__text">La placa base, también conocida como placa madre, es el componente
                        principal de una
                        computadora que proporciona la conexión física y eléctrica entre otros componentes, como el
                        procesador, la memoria, las tarjetas de expansión y los dispositivos de entrada/salida. </p>
                </article>
                <div class="clear"></div>
                <article class="temario__article">
                    <img class="temario__img temario__img--low temario__img--ltr"
                        src="/kat/resource/img/temario/procesador.jpg" alt="" srcset="">
                    <h3 class="article__title">2.2 El Procesador: El cerebro de la operación
                    </h3>
                    <p class="article__text">El procesador, también conocido como Unidad Central de Procesamiento
                        (CPU), es el componente
                        principal de una computadora responsable de ejecutar instrucciones y procesar datos.
                        Funciona como el cerebro de la computadora, realizando operaciones aritméticas, lógicas y de
                        control según lo indicado por los programas en ejecución.</p>
                </article>
                <div class="clear"></div>
                <article class="temario__article">
                    <img class="temario__img temario__img--low temario__img--rtl" src="/kat/resource/img/temario/ram.png"
                        alt="" srcset="">
                    <h3 class="article__title">2.3 La Memoria RAM: Almacenamiento temporal para la acción</h3>
                    <p class="article__text">
                        La memoria RAM (Memoria de Acceso Aleatorio) es un tipo de memoria volátil que se utiliza en
                        computadoras y otros dispositivos electrónicos para almacenar temporalmente datos y
                        programas que están en uso. Es accesible de manera aleatoria, lo que significa que el
                        sistema puede leer o escribir en cualquier ubicación de la memoria de forma directa y
                        rápida. <br> La RAM es fundamental para el funcionamiento de un sistema informático, ya que
                        permite a la CPU acceder rápidamente a los datos necesarios para ejecutar programas y
                        realizar operaciones, mejorando así el rendimiento general del sistema.</p>
                </article>
                <div class="clear"></div>
                <article class="temario__article">
                    <img class="temario__img  temario__img--ltr" src="/kat/resource/img/temario/discos.png" alt=""
                        srcset="">
                    <h3 class="article__title">2.4 El Almacenamiento: Hogar permanente de tus datos
                    </h3>
                    <p class="article__text">El almacenamiento de computadora se refiere a la capacidad de guardar
                        datos de manera
                        permanente en un dispositivo electrónico. Este almacenamiento puede ser tanto interno como
                        externo y se utiliza para conservar información incluso cuando la computadora está apagada.
                        Los dispositivos de almacenamiento comunes incluyen discos duros, unidades de estado sólido
                        (SSD), unidades flash USB, tarjetas de memoria y discos ópticos como CD, DVD y Blu-ray. El
                        almacenamiento de la computadora permite a los usuarios guardar archivos, programas y otros
                        datos para su acceso posterior, lo que lo convierte en una parte fundamental de cualquier
                        sistema informático.</p>
                </article class="temario__article">
                <div class="clear"></div>
                <article class="temario__article">
                    <img class="temario__img temario__img--rtl" src="/kat/resource/img/temario/fuente.png" alt=""
                        srcset="">
                    <h3 class="article__title">2.5 La fuente de alimentación
                    </h3>
                    <p class="article__text">La fuente de alimentación de un computador es un componente fundamental
                        que suministra
                        energía eléctrica a todos los demás componentes del sistema. Se encarga de convertir la
                        corriente alterna proveniente de la toma de corriente en corriente continua de bajo voltaje,
                        necesaria para el funcionamiento de la computadora. </p>
                </article>
